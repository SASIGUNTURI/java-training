package assessment;
    public class Swapping
    {
        public static void main(String[] args) {
            int i=10,j=20,t;
            System.out.println("before swapping numbers: "+i +" and "+ j);
            t = i;
            i = j;
            j = t;
            System.out.println("After swapping numbers: "+i +" and " + j);
        }
    }


